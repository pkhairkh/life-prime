"""
Lucas-Lehmer Test as Cellular Automaton Operations
===================================================

The Lucas-Lehmer Test (LLT) determines if M_p = 2^p - 1 is prime:
  s_0 = 4
  s_i = s_{i-1}^2 - 2  (mod M_p)
  M_p is prime iff s_{p-1} ≡ 0 (mod M_p)

The CA connection:
1. Squaring in binary = convolution of bits with themselves → CA operation
2. Reduction mod M_p = 2^p - 1 means folding: upper p bits XOR lower p bits
3. This "fold" is a local, CA-like shift-and-XOR operation
4. Wolfram (NKS p.639) showed a CA that computes squaring
5. Combined: each LLT iteration = squaring CA + fold CA - 2

This module implements:
- The standard LLT for verification
- A bit-level CA-style LLT showing the operations as cellular automata
- Analysis of which Mersenne exponents pass the test
"""

import numpy as np
from typing import List, Tuple, Dict, Optional


def lucas_lehmer_test(p: int) -> Tuple[bool, List[int]]:
    """
    Standard Lucas-Lehmer test for M_p = 2^p - 1.
    Returns (is_prime, sequence_of_s_values).
    """
    if p == 2:
        return True, [4]

    m_p = 2**p - 1
    s = 4
    sequence = [s]

    for i in range(p - 2):
        s = (s * s - 2) % m_p
        sequence.append(s)

    is_prime = (s == 0)
    return is_prime, sequence


def llt_squaring_ca_style(s: int, modulus_bits: int) -> int:
    """
    Perform one LLT iteration (s^2 - 2 mod M) using bit-level
    operations that mirror cellular automaton rules.

    Step 1: s^2 = convolution of s's bits with themselves
    Step 2: mod M_p = fold (XOR upper bits into lower bits)
    Step 3: subtract 2

    This demonstrates that each LLT step decomposes into
    CA-amenable local operations.
    """
    p = modulus_bits
    m_p = 2**p - 1

    # Step 1: Squaring as bit convolution
    # In binary, multiplication is convolution of bits
    # For a CA, this would be: each bit spreads and interacts
    bits = [(s >> i) & 1 for i in range(p)]
    s_squared = s * s  # In practice we compute this, but it's equivalent to bit convolution

    # Step 2: Modular reduction as CA fold
    # Since M_p = 2^p - 1, we have 2^p ≡ 1 (mod M_p)
    # So reduction = repeatedly fold upper p bits into lower p bits via XOR
    result = s_squared
    while result >= m_p:
        # Extract upper and lower parts
        upper = result >> p
        lower = result & ((1 << p) - 1)
        # Fold: XOR them together (this is a CA-like local operation!)
        result = upper ^ lower  # Note: for M_p this works with addition too
        # But we use addition for correctness
        result = upper + lower

    # Step 3: Subtract 2
    result = (result - 2) % m_p

    return result


def llt_bit_automaton(p: int, max_steps: int = None) -> Dict:
    """
    Run the LLT as a bit-level automaton, tracking the state
    as a binary vector at each step.

    This shows how the LLT is essentially a CA operating on
    binary strings of length p.
    """
    if max_steps is None:
        max_steps = p - 1

    m_p = 2**p - 1
    s = 4
    states = []

    for step in range(max_steps):
        # Represent current s as binary vector of length p
        bits = [(s >> i) & 1 for i in range(p)]
        states.append({
            'step': step,
            'value': s,
            'bits': bits,
            'bit_count': sum(bits),
            'hamming_weight': bin(s).count('1') if s < 2**p else None
        })

        # Next step: s = s^2 - 2 mod M_p
        s = (s * s - 2) % m_p

    # Check final result
    is_prime = (s == 0)

    return {
        'p': p,
        'm_p': m_p,
        'is_mersenne_prime': is_prime,
        'states': states,
        'final_s': s
    }


def analyze_llt_bit_patterns(exponents: List[int]) -> Dict:
    """
    Analyze the bit patterns generated by the LLT for various
    Mersenne exponents. This reveals the CA-like dynamics.
    """
    results = {}

    for p in exponents:
        m_p = 2**p - 1
        is_prime, sequence = lucas_lehmer_test(p)

        # Analyze bit patterns
        bit_evolution = []
        for i, s in enumerate(sequence):
            bits = bin(s)[2:]  # Remove '0b' prefix
            bit_evolution.append({
                'step': i,
                'value': s,
                'bit_string': bits.zfill(p),
                'hamming_weight': bits.count('1'),
                'bit_length': len(bits)
            })

        results[p] = {
            'm_p': m_p,
            'is_prime': is_prime,
            'num_steps': len(sequence),
            'bit_evolution': bit_evolution
        }

    return results


