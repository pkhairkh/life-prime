def analyze_llt_orbit_structure(p: int) -> Dict:
    """
    Analyze the orbit structure of the map x → x² - 2 (mod M_p).
    For Mersenne primes, this map has special tree-like structure
    (related to the diGraph analysis by Terence Tao).
    """
    m_p = 2**p - 1

    # Track where the LLT sequence visits
    is_prime, sequence = lucas_lehmer_test(p)

    # Compute the functional graph for small M_p
    if m_p <= 10000:
        visited = {}
        for x in range(m_p):
            y = (x * x - 2) % m_p
            visited[x] = y

        # Find cycles
        cycles = []
        seen = set()
        for start in range(m_p):
            if start in seen:
                continue
            path = []
            current = start
            while current not in path:
                if current in seen:
                    break
                path.append(current)
                seen.add(current)
                current = visited[current]
            else:
                cycle_start = path.index(current)
                cycle = path[cycle_start:]
                if len(cycle) > 0:
                    cycles.append(cycle)
                for node in path[:cycle_start]:
                    pass  # These are on trees leading to the cycle

        return {
            'p': p,
            'm_p': m_p,
            'is_prime': is_prime,
            'num_cycles': len(cycles),
            'cycle_lengths': sorted([len(c) for c in cycles], reverse=True),
            'llt_sequence_length': len(sequence),
        }

    return {
        'p': p,
        'm_p': m_p,
        'is_prime': is_prime,
        'llt_sequence_length': len(sequence),
        'orbit_analysis': 'M_p too large for full analysis'
    }


